
# Assim que faz pra importar arquivos de pastas:



# A pasta que contem os arquivos precisa também ter um arquivo chamado __init__.py (são 2 underlines no começo e 2 no final)
# O python usa isso pra saber quais pastas procurar arquivos com código.
# o arquivo __init__.py em si, geralmente estará vazio

# nesse exemplo, eu criei uma pasta "Funcao", dentro criei um arquivo "prints.py" com as funções que eu queria
# e também coloquei um __init__py vazio.
# então só importei "from Funcao import prints"