
# 3 jeitos de fazer um print mais facilmente


# print normal
nome = 'greg'
print("seu nome é +"+nome+", seja bem vindo ")


# 1 - da pra usar F antes das aspas para facilitar o uso de váriaveis 
nome = 'greg'
print(f"seu nome é {nome}, seja bem vindo ")


# 2 - da pra usar """ 3 aspas para fazer um print com várias linhas 
print("""Greg(G) 
  ATAQUE: 3
  DEFESA: 1
  VIDA: 8
  INTELIGENCIA: 2
""")


# 3 - da pra usar os dois 
print(f"""{nome}(G)) 
  ATAQUE: 3
  DEFESA: 1
  VIDA: 8
  INTELIGENCIA: 2
""")